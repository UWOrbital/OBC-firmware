#define STOP_ON_ERROR(text, _ret)                   \
  errCode = _ret;                                   \
  if (errCode != OBC_ERR_CODE_SUCCESS) {            \
    sciPrintf("%s returned %d\r\n", text, errCode); \
    sciPrintf("Stopping example\r\n");              \
    while (1)                                       \
      ;                                             \
  }                                                 \
  sciPrintf("%s was successful\r\n", text)

